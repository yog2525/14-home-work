
public class OddOrEven {


        public static void main(String[] args){
        {
            int number = 3;     // in put any number to find odd or even//
            if((number % 2) == 0){
                System.out.println(number +" is even");
            }
            else{
                System.out.println(number +" is odd");
            }

        }
    }}
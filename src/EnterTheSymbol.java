
public class EnterTheSymbol {
    public static void main(String[] args){

        double a = 20;
        double b = 10;

        char symbol = '-' ; // input symbol to find value//

        if (symbol =='+') {
            System.out.println(a+b);
        }
        else if (symbol =='-'){
            System.out.println(a-b);}
        else if (symbol =='/'){
            System.out.println(a/b);}
        else if (symbol =='*'){
            System.out.println(a*b);}

    }

}




public class interchangedvalue {

    public static void main(String[] args){
        int a =  20;
        int b =  30;

        System.out.println("value of a is a: "+a+" value of b:"+b+"");

        a=a+b;
        b=a-b;
        a=a-b;

        System.out.println("value of a is a: "+a+" value of b:"+b+"");


    }







}
